const OUTPUT_DIR = path.join(process.cwd(), 'src', 'data');

function ensureOutputDir(): void {
  if (!fs.existsSync(OUTPUT_DIR)) {
    fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  }
}

function writeDataFile(filename: string, data: unknown): void {
  const filePath = path.join(OUTPUT_DIR, filename);
  const content = `// Auto-generated on ${new Date().toISOString()}
// DO NOT EDIT MANUALLY - This file is generated by scripts/fetch-data.ts

export const ${filename.replace('.ts', '').replace(/-/g, '_')} = ${JSON.stringify(data, null, 2)} as const;
`;
  fs.writeFileSync(filePath, content);
  console.log(`✓ Written: ${filePath}`);
}

async function fetchWithRetry(url: string, retries = 3): Promise<Response> {
  for (let i = 0; i < retries; i++) {
    try {
      const response = await fetch(url, {
        headers: {
          'User-Agent': 'UK-Welfare-Stats-Bot/1.0 (https://github.com/UKStatsHub/welfare)',
          'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
        },
      });
      if (response.ok) return response;
      console.log(`Attempt ${i + 1} failed: ${response.status}`);
    } catch (error) {
      console.log(`Attempt ${i + 1} error: ${error}`);
    }
    await new Promise(r => setTimeout(r, 1000 * (i + 1)));
  }
  throw new Error(`Failed to fetch ${url} after ${retries} attempts`);
}

function parseNumber(value: string | undefined | null): number | null {
  if (!value) return null;
  const cleaned = value.replace(/[^0-9.-]/g, '');
  const num = parseFloat(cleaned);
  return isNaN(num) ? null : num;
}

function formatNumber(num: number | null): string {
  if (num === null) return 'N/A';
  if (num >= 1000000) return `${(num / 1000000).toFixed(1)}m`;
  if (num >= 1000) return `${(num / 1000).toFixed(0)}k`;
  return num.toLocaleString('en-GB');
}

